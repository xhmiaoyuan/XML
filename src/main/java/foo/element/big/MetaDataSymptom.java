package foo.element.big;


public class MetaDataSymptom extends MetaDataForAll {

	String Cause;
	String Disgnosis;
	String Therapy;
	public String getCause() {
		return Cause;
	}
	public void setCause(String cause) {
		Cause = cause;
	}
	public String getDisgnosis() {
		return Disgnosis;
	}
	public void setDisgnosis(String disgnosis) {
		Disgnosis = disgnosis;
	}
	public String getTherapy() {
		return Therapy;
	}
	public void setTherapy(String therapy) {
		Therapy = therapy;
	}
	
}
